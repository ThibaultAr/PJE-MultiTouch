package mygeom;

public class OBB {
	protected float angle;
	protected Vector2 origin;
	protected float width;
	protected float height;
	
	
	public float getAngle() {
		return angle;
	}
	public Vector2 getOrigin() {
		return origin;
	}
	public float getWidth() {
		return width;
	}
	public float getHeight() {
		return height;
	}
}
